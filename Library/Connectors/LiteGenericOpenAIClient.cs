using OpenAI;
using OpenAI.Chat;
using System.ClientModel;
using LiteAgent.Constants;

namespace LiteAgent.Connectors;

/// <summary>
/// A generic AI client compatible with the OpenAI API standard.
/// This client can be used with OpenAI, Ollama, Groq, and other local or third-party providers.
/// </summary>
public class LiteGenericOpenAIClient : ILiteClient
{
    private readonly ChatClient _chatClient;
    private int _maxTokens = 1000;
    private float _temperature = 0.7f;

    /// <summary>
    /// Initializes a new instance of the Generic OpenAI client.
    /// </summary>
    /// <param name="apiKey">The API Key for the service. For local services like Ollama, this can be "ollama" or any string.</param>
    /// <param name="modelId">The model identifier (e.g., "gpt-4o", "llama3", "mixtral").</param>
    /// <param name="endpoint">The base URL of the service. Default is the official OpenAI endpoint.</param>
    public LiteGenericOpenAIClient(string apiKey, string modelId, string endpoint = "https://api.openai.com/v1")
    {
        var options = new OpenAIClientOptions
        {
            Endpoint = new Uri(endpoint)
        };

        _chatClient = new ChatClient(modelId, new ApiKeyCredential(apiKey), options);
    }

    /// <summary>
    /// Sends the conversation history to the AI model and retrieves a completion.
    /// </summary>
    /// <param name="history">The list of messages representing the conversation state.</param>
    /// <returns>The text response generated by the model.</returns>
    public async Task<string> GetCompletionAsync(List<LiteMessage> history)
    {
        var messages = new List<ChatMessage>();

        // Map internal LiteMessage to OpenAI ChatMessage
        foreach (var message in history)
        {
            if (message.Role == Roles.System)
                messages.Add(new SystemChatMessage(message.Content));
            else if (message.Role == Roles.Assistant)
                messages.Add(new AssistantChatMessage(message.Content));
            else
                messages.Add(new UserChatMessage(message.Content));
        }

        var options = new ChatCompletionOptions
        {
            Temperature = _temperature,
            MaxOutputTokenCount = _maxTokens
        };

        var response = await _chatClient.CompleteChatAsync(messages, options);

        return response.Value.Content[0].Text;
    }

    /// <summary>
    /// Updates the maximum number of tokens for the generated response.
    /// </summary>
    public void SetMaxTokens(int maxTokens) => _maxTokens = maxTokens;

    /// <summary>
    /// Updates the temperature parameter for the model's creativity.
    /// </summary>
    public void SetTemperature(float temperature) => _temperature = temperature;
}